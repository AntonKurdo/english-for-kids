function themeTurn() {
    if (!state.isTrainMode) {
        document.querySelector('nav').classList.add('play_mode')
        document.querySelectorAll('.card').forEach(item => {
            item.classList.add('play_mode');
        })

    }

    if (state.isTrainMode) {
        document.querySelector('nav').classList.remove('play_mode')
        document.querySelectorAll('.card').forEach(item => {
            item.classList.remove('play_mode');
        })
    }

}


function renderCardsInPM(arr) {
    document.querySelector('.main').innerHTML = '';
    const card = document.querySelector('.main');
    let html = arr.map(item =>
        `<div class='main_cont_flip'>
 <div class='front'>
     <div id='${item.id}' class="card border-info mb-3" style="max-width: 20rem;">
         <div id='${item.id}' class="card-body">
             <div id='${item.id}' class="img_cont"><img id='${item.id}' src="${item.img}" alt=""></div>
         </div>
     </div>
 </div>
</div>`
    )
    card.innerHTML = html.join(' ') + `<div class= 'btn_start_game_cont'> <button class = 'btn btn_start_game btn-danger' id = ${arr[0].arrayId}> Start New Game </button> </div>`;

}




